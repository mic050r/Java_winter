public class Four_op_1116{
   public static void main(String args[]){
        double num1,num2;
        num1 = 3.5;
        num2 = 7.2;
        System.out.println(num1 + "+" + num2 + "=" + (num2+num2));
        System.out.println(num1 + "-" +num2 +"=" + (num1-num2));
        System.out.println(num1 + "*" + num2 + "=" + (num1*num1));
        System.out.println(num1 + "/" + num2 +"=" + (num1/num2));
 
   }
}